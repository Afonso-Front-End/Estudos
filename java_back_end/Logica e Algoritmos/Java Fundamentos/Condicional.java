public class Condicional {
    public static void main(String[] args) {
        
        //variavel de idade
        int idade = 20;

        //condicional
        if(idade >= 18){
            System.out.println("Maior de idade");
        }else{
            System.out.println("Menor de idade");
        }
    }
    /*
        opredaores relacionais 
        >   maior que
        <   menor que
        >=  maior ou igual
        <=  menor ou igual
        ==  igual(verifica o valor)
        ==  igual(veirifica o valor eo tipo de variavel)
        !=  diferente
    */
}
